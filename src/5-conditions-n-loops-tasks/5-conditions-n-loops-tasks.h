/*
* Returns max digit by given number

* example:
    (1) => 1
    (23) => 3
    (546) => 6
    (999) => 9
 */
int getMaxDigit(int);

/*
* Returns true if the given number is prime; otherwise false.

* example:
    (2) => true
    (13) => true
    (14) => false
    (97) => true
*/
bool isPrime(int);

/*
* Return greatest common divisor of two number;

* example:
    (15, 25) => 5
    (9, 10) => 1
    (3, 15) => 3
*/
int getGreatestCommonDivisor(int, int);
